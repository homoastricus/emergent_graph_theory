# –§–£–ù–î–ê–ú–ï–ù–¢–ê–õ–¨–ù–´–ô –ê–¢–¢–†–ê–ö–¢–û–† –í–°–ï–õ–ï–ù–ù–û–ô
def universe_attractor():
    K = 8.0
    e = math.exp(1)

    print("=" * 70)
    print("–ê–¢–¢–†–ê–ö–¢–û–† –í–°–ï–õ–ï–ù–ù–û–ô: p‚àö(KU) ‚Üí e")
    print("=" * 70)

    # 1. –¢–µ–∫—É—â–µ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    p_now = 0.0527
    N_now = 0.95e123
    U_now = math.log(N_now) / abs(math.log(K * p_now))
    attractor_value_now = p_now * math.sqrt(K * U_now)

    print(f"1. –°–ï–ô–ß–ê–° (t=13.8 –º–ª—Ä–¥ –ª–µ—Ç):")
    print(f"   p = {p_now:.6f}, N = {N_now:.2e}")
    print(f"   U = ln(N)/|ln(Kp)| = {U_now:.3f}")
    print(f"   p‚àö(KU) = {attractor_value_now:.10f}")
    print(f"   e = {e:.10f}")
    print(f"   –û—à–∏–±–∫–∞: {(attractor_value_now / e - 1) * 100:.4f}%")
    print(f"   ‚Üí –í 1.4% –æ—Ç –∞—Ç—Ç—Ä–∞–∫—Ç–æ—Ä–∞")

    # 2. –ê—Ç—Ç—Ä–∞–∫—Ç–æ—Ä–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    p_attr = 0.05
    # –ò–∑ e = p‚àö(KU) => U = e¬≤/(p¬≤K)
    U_attr = e ** 2 / (p_attr ** 2 * K)
    # –ò–∑ U = ln(N)/|ln(Kp)| => ln(N) = U √ó |ln(Kp)|
    N_attr = math.exp(U_attr * abs(math.log(K * p_attr)))

    print(f"\n2. –ê–¢–¢–†–ê–ö–¢–û–† (t‚Üí‚àû):")
    print(f"   p ‚Üí {p_attr:.6f}")
    print(f"   U = e¬≤/(p¬≤K) = {U_attr:.3f}")
    print(f"   N = exp(U√ó|ln(Kp)|) = {N_attr:.2e}")
    print(f"   –ü—Ä–æ–≤–µ—Ä–∫–∞: p‚àö(KU) = {p_attr * math.sqrt(K * U_attr):.15f}")
    print(f"   e = {e:.15f}")
    print(f"   ‚Üí –ê–ë–°–û–õ–Æ–¢–ù–û –¢–û–ß–ù–û!")

    # 3. –≠–≤–æ–ª—é—Ü–∏—è
    print(f"\n3. –≠–í–û–õ–Æ–¶–ò–Ø –ö –ê–¢–¢–†–ê–ö–¢–û–†–£:")
    print(f"   Œîp = {p_now - p_attr:.6f} ({((p_now - p_attr) / p_now * 100):.2f}% —É–º–µ–Ω—å—à–µ–Ω–∏–µ)")
    print(f"   ŒîN = {N_attr / N_now:.2e} (–≤ {N_attr / N_now:.0f} —Ä–∞–∑ –±–æ–ª—å—à–µ)")
    print(f"   ŒîU = {U_attr / U_now:.3f} (U —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç—Å—è –≤ {U_attr / U_now:.2f} —Ä–∞–∑–∞)")

    return {
        'now': {'p': p_now, 'N': N_now, 'U': U_now},
        'attractor': {'p': p_attr, 'N': N_attr, 'U': U_attr}
    }


