def numerical_analysis():
    """–ß–∏—Å–ª–µ–Ω–Ω—ã–π –∞–Ω–∞–ª–∏–∑ —Ç–æ—á–Ω–æ—Å—Ç–∏ —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏–π"""

    print("=== –ß–ò–°–õ–ï–ù–ù–´–ô –ê–ù–ê–õ–ò–ó –¢–û–ß–ù–û–°–¢–ò ===")

    K = 8.0
    p = 0.05
    e = math.exp(1)
    U = e ** 2 / (p ** 2 * K)

    # –í—ã—á–∏—Å–ª–∏–º –≤—Å–µ –≤–µ–ª–∏—á–∏–Ω—ã
    sqrt_KU = math.sqrt(K * U)
    inv_sqrt_KU = 1 / sqrt_KU

    print("–í—ã—á–∏—Å–ª–µ–Ω–Ω—ã–µ –≤–µ–ª–∏—á–∏–Ω—ã:")
    print(f"U = {U:.15f}")
    print(f"‚àö(KU) = {sqrt_KU:.15f}")
    print(f"1/‚àö(KU) = {inv_sqrt_KU:.15f}")
    print(f"KU = {K * U:.15f}")
    print(f"p/e = {p / e:.15f}")
    print(f"e/p = {e / p:.15f}")
    print()

    # –ü—Ä–æ–≤–µ—Ä–∏–º –≤—Å–µ —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏—è —Å –≤—ã—Å–æ–∫–æ–π —Ç–æ—á–Ω–æ—Å—Ç—å—é
    relations = []

    # 1. K + 1/‚àö(KU) = K + p/e
    r1_left = K + inv_sqrt_KU
    r1_right = K + p / e
    relations.append(("K + 1/‚àö(KU) = K + p/e", r1_left, r1_right))

    # 2. ‚àö(KU) = KU + 1/‚àö(KU)
    r2_left = sqrt_KU
    r2_right = K * U + inv_sqrt_KU
    relations.append(("‚àö(KU) = KU + 1/‚àö(KU)", r2_left, r2_right))

    # 3. 8(œÄ+U) = exp(8)
    r3_left = 8 * (math.pi + U)
    r3_right = math.exp(8)
    relations.append(("8(œÄ+U) = exp(8)", r3_left, r3_right))

    # 4. e‚Åª¬π = (1/‚àö(KU))/p
    r4_left = 1 / e
    r4_right = inv_sqrt_KU / p
    relations.append(("e‚Åª¬π = (1/‚àö(KU))/p", r4_left, r4_right))

    print("–¢–û–ß–ù–´–ï –†–ê–°–ß–ï–¢–´:")
    print(f"{'–°–æ–æ—Ç–Ω–æ—à–µ–Ω–∏–µ':<30} | {'–õ–µ–≤–∞—è':<20} | {'–ü—Ä–∞–≤–∞—è':<20} | {'–û—à–∏–±–∫–∞ %':<12}")
    print("-" * 90)

    for name, left, right in relations:
        error = abs(left / right - 1) * 100
        print(f"{name:<30} | {left:<20.15f} | {right:<20.15f} | {error:<12.10f}")

    # –°—Ä–µ–¥–Ω—è—è –æ—à–∏–±–∫–∞
    avg_error = sum(abs(l / r - 1) * 100 for _, l, r in relations) / len(relations)
    print(f"\n–°—Ä–µ–¥–Ω—è—è –æ—à–∏–±–∫–∞: {avg_error:.10f}%")

    return relations


